// 계산기
int calc()
{
	printf("계산할 연산자를 입력하세요.\n");

	char c = ' ';
	scanf(" %c", &c);

	if (c == '+')
		add();
	else if (c == '-')
		sub();
	else if (c == '*')
		mul();
	else if (c == '/')
		div();
	else if (c == '%')
		rem();
	else
		printf("입력이 잘못됐습니다.\n\n");

	return 0;
}

