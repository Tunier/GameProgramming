// 나누고 값을 출력.
int div()
{
	printf("나눌 숫자 2개를 입력하세요.\n");

	int i, j;

	scanf(" %d %d", &i, &j);

	if (j == 0)
	{
		printf("분모는 0이될수 없습니다.\n");
		return 0;
		// 함수 중간에 리턴이 들어갔기 때문에
		// 해당 조건 하에서는 함수가 여기서 종료되고
		// 아래의 코드는 실행되지 않는다.
	}

	printf("%d / %d = %d 입니다.\n", i, j, i / j);

	return 0;
}
